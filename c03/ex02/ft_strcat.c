char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	m;

	i = 0;
	m = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	while (src[m] != '\0')
	{
		dest[i] = src[m];
		i++;
		m++;
	}
	dest[i] = '\0';
	return (dest);
}
